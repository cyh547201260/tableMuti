function checkboxRenderer(instance, td, row, col, prop, value, cellProperties){
	if(row  == 0 || row  == 1){
		td.className = 'readonly-back';
		return td;
	}
	
	var dom = document.createElement('input');
	dom.setAttribute('type','checkbox');
	dom.setAttribute('data-row',row);
	dom.className = 'item-checker item-checker-li-'+row;
	dom.style = 'vertical-align: top';
	
	if($(td).children('input').length > 0){
		var checkObj = document.getElementsByClassName('item-checker-li-'+row);
		if($(checkObj[1]).prop('checked')){
			dom.setAttribute('checked','checked');
		}
		$(td).html(dom);
	}else{
		$(td).html(dom);
	}
	
  	return td;
}


