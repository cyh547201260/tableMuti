//表格操作按钮
function tableCfgBtn(type,row){
	if(type == 2){
		swal({
		  title: "提示",
		  text: "确定删除此条记录吗",
		  icon: "warning",
		  buttons: true,
		  dangerMode: true,
		})
		.then(function(willDelete) {
		  if (willDelete) {
		  	deleteRowData(row)
		    swal("删除成功", {
		      icon: "success",
		    });
		  } 
		});
	}
}

